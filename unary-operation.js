function unarPlus(num) {
    return +num
}
console.log(unarPlus(5));

function unarMinus(num) {
    return -num
}
console.log(unarMinus(5));


function increment(num) {
    return ++num
}
console.log(increment(5));

function decrement(num) {
    return --num
}
console.log(decrement(5));


// unary plus - исходное число
// unary minus - отрицательное число
// increment - увеличивает число на 1 (+1)
// decrement - уменьшает чило на 1 (-1)